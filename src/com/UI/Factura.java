 private int indiceTorta(String codigo, LinkedList<Torta> SusTortas) {
      
        int res = -1;
        
        int i = 0;
        
        for (Torta t: SusTortas){
            
            if (t.getCodigo()==codigo){
            
            
                
                return i;
            }
            
            i ++;
            
        }
        
        return res;
        
        
    }

